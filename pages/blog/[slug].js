import Head from "next/head";
import { format, parseISO } from "date-fns";
import { serialize } from "next-mdx-remote/serialize";
import { MDXRemote } from "next-mdx-remote";

import { getAllPosts } from "../../lib/data";

export default function BlogPage({ title, date, content }) {
  return (
    <div className="max-h-fit">
      <Head>
        <title>albac: {title}</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main>
        <div className="container dark:bg-slate-900">
          <div className="border-b-2 border-gray-400 my-4">
            <h2 className="dark:text-white text-3xl font-bold">{title}</h2>
            <div className="text-sm text-gray-600 dark:text-gray-200 mt-4">
              {format(parseISO(date), "MMMM do, uuu")}
            </div>
          </div>
          <article className="prose dark:prose-invert text-slate-600 font-light font-sans">
            <MDXRemote {...content} />
          </article>
        </div>
      </main>
    </div>
  );
}

export async function getStaticProps(context) {
  // console.log(context);
  const { params } = context;
  const allPosts = getAllPosts();
  const { data, content } = allPosts.find((item) => item.slug === params.slug);
  // console.log(data, content);
  const mdxSource = await serialize(content, { parseFrontmatter: true });

  return {
    props: {
      ...data,
      date: data.date.toISOString(),
      content: mdxSource,
    },
  };
}

export async function getStaticPaths() {
  return {
    paths: getAllPosts().map((post) => ({
      params: {
        slug: post.slug,
      },
    })),
    fallback: false,
  };
}
